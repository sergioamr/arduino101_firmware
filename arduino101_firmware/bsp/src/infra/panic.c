#include "os/os.h"
#include "infra/panic.h"
#include "infra/log.h"

#define PANIC_UNKNOWN    (-1)       /*!< Default panic error code */

void __attribute__((weak)) handle_panic_notification(int core_id)
{
    panic(core_id);
}

void __attribute__((weak)) __assert_hook(const char *_file, int _line, const char *_func, const char *_expr )
{
    pr_error(LOG_MODULE_MAIN, "panic (%s) File: (%s) line: (%d) Function (%s)\n", _expr, _file, _line, _func);
    panic(E_OS_ERR);
}
